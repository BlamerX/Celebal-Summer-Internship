'''
Arithmetic Operators: Arithmetic operators are used to perform mathematical operations on numerical values. Python supports the following arithmetic operators:

* Addition (+): Adds two operands.

* Subtraction (-): Subtracts the second operand from the first.

* Multiplication (*): Multiplies two operands.

* Division (/): Divides the first operand by the second.

* Modulus (%): Returns the remainder of the division of the first operand by the second.

* Exponentiation (**): Raises the first operand to the power of the second.

* Floor Division (//): Returns the quotient of the division of the first operand by the second, rounded down to the nearest integer.
'''

a = 10
b = 3

print(a + b)  # Output: 13
print(a - b)  # Output: 7
print(a * b)  # Output: 30
print(a / b)  # Output: 3.3333333333333335
print(a % b)  # Output: 1
print(a ** b)  # Output: 1000
print(a // b)  # Output: 3
