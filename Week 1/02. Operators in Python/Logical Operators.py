'''
Logical Operators: Logical operators are used to combine two or more conditions. Python supports the following logical operators:

* And: Returns True if both operands are True.

* Or: Returns True if at least one of the operands is True.

* Not: Returns True if the operand is False.
'''

a = 10
b = 3
c = 5

print(a > b and b < c)  # Output: True
print(a > b or b > c)   # Output: True
print(not (a, b))       # Output: False
