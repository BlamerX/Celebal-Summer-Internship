'''
For Loops in Python

For loops are used to iterate over a sequence of values.
In Python, you can use a for loop to iterate over a list, tuple, set, or string.'''

# Example of using a for loop with a list
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)

# Example of using a for loop with a range of numbers
for i in range(5):
    print(i)

# Example of using a for loop with a string
message = "Hello, world!"
for char in message:
    print(char)
